import { GoogleGenerativeAI } from "@google/generative-ai";
import { Resend } from 'resend';
import { inngest } from "./client";

// Initialize Resend
const resend = new Resend(process.env.RESEND_API_KEY);

// Initialize Gemini AI
const genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
const model = genAI.getGenerativeModel({ model: "gemini-1.5-flash" });

/**
 * Get the first day of the previous month in YYYY-MM-DD format
 */
function getFirstDayOfLastMonth() {
  const date = new Date();
  date.setMonth(date.getMonth() - 1);
  date.setDate(1);
  return date.toISOString().split('T')[0];
}

export const spendingInsights = inngest.createFunction(
  { name: "Generate Spending Insights", id: "generate-spending-insights" },
  { cron: "0 8 1 * *" }, // 1st of every month at 08:00
  async ({ step }) => {
    const lastMonthStart = getFirstDayOfLastMonth();
    
    /* ─── 1. Pull users with expenses last month ────────────────────── */
    const users = await step.run("Fetch users with expenses", async () => {
      const response = await fetch(
        `${process.env.NEXT_PUBLIC_API_URL}/api/inngest/users-with-expenses?month=${lastMonthStart}`
      );
      if (!response.ok) {
        throw new Error(`Failed to fetch users with expenses: ${response.statusText}`);
      }
      return response.json();
    });

    /* ─── 2. Iterate users & send insight email ─────────────────────── */
    const results = [];

    for (const user of users) {
      /* a. Pull last-month expenses (skip if none) */
      const expenses = await step.run(`Fetch expenses · ${user._id}`, async () => {
        const response = await fetch(
          `${process.env.NEXT_PUBLIC_API_URL}/api/inngest/user-monthly-expenses?userId=${user._id}&month=${lastMonthStart}`
        );
        if (!response.ok) {
          console.error(`Failed to fetch expenses for user ${user._id}:`, response.statusText);
          return [];
        }
        return response.json();
      });
      
      if (!expenses?.length) continue;

      /* b. Build JSON blob for the prompt */
      const expenseData = JSON.stringify({
        expenses,
        totalSpent: expenses.reduce((sum, e) => sum + e.amount, 0),
        categories: expenses.reduce((cats, e) => {
          const category = e.category || "uncategorised";
          cats[category] = (cats[category] || 0) + e.amount;
          return cats;
        }, {}),
      });

      /* c. Prompt + AI call using step.ai.wrap (retry-aware) */
      const prompt = `
As a financial analyst, review this user's spending data for the past month and provide insightful observations and suggestions.
Focus on spending patterns, category breakdowns, and actionable advice for better financial management.
Use a friendly, encouraging tone. Format your response in HTML for an email.

User spending data:
${expenseData}

Provide your analysis in these sections:
1. Monthly Overview
2. Top Spending Categories
3. Unusual Spending Patterns (if any)
4. Saving Opportunities
5. Recommendations for Next Month
      `.trim();

      try {
        const aiResponse = await step.ai.wrap(
          "gemini",
          async (p) => model.generateContent(p),
          prompt
        );

        const htmlBody = aiResponse.response.candidates[0]?.content.parts[0]?.text || "";

        if (!htmlBody) {
          throw new Error("No content generated by AI");
        }

        /* d. Send the email using Resend */
        await step.run(`Email · ${user._id}`, async () => {
          try {
            await resend.emails.send({
              from: process.env.RESEND_FROM_EMAIL || 'Splitr Insights <insights@splitr.example.com>',
              to: user.email,
              subject: "Your Monthly Spending Insights",
              html: `
                <h1>Your Monthly Financial Insights</h1>
                <p>Hi ${user.name},</p>
                <p>Here's your personalized spending analysis for the past month:</p>
                ${htmlBody}
                <p>— The Splitr Team</p>
              `
            });
          } catch (emailError) {
            console.error(`Failed to send email to ${user.email}:`, emailError);
            throw emailError;
          }
        });

        results.push({ userId: user._id, success: true });
      } catch (err) {
        console.error(`Error generating/sending insights for user ${user._id}:`, err);
        results.push({
          userId: user._id,
          success: false,
          error: err.message,
        });
      }
    }

    /* ─── 3. Summary for the cron log ───────────────────────────────── */
    return {
      processed: results.length,
      success: results.filter((r) => r.success).length,
      failed: results.filter((r) => !r.success).length,
    };
  }
);
